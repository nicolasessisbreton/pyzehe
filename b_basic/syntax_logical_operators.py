# basic comparison
x = 1
y = 2
print('x<=y', x<=y)
print('x<y', x<y)
print('x==y', x==y)
print('x>y', x>y)
print('x>=y', x>=y)

# logical test
x = False
y = True
print('not x', not x)
print('~x', ~x)

print('x or y', x or y)
print('x | y', x | y)

print('x and y', x and y)
print('x & y', x & y)

# other operator
x = 1
print('x is 1', x is 1)
print('x is not 2', x is not 2)

x = 'abc'
print('a in abc', 'a' in x)
print('d not in abc', 'd' not in x)
